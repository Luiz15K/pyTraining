def soma(valor1, valor2):
    return int(valor1) + int(valor2)

def subtrai(valor1, valor2):
    return int(valor1) - int(valor2)

def divisao(valor1, valor2):
    if valor2 != 0:
        return int(valor1) / int(valor2)
    else:
        print('Error')
        
def multiplicacao(valor1, valor2):
    return int(valor1) * int(valor2)
